#!/bin/sh
set -eu

######################################################################
# 設定
######################################################################

infile="text/icon.txt"
outfile="crd/crd.txt"

######################################################################
# 本体処理
######################################################################

cat "$infile"                                                        |
draw2crd.sh -s""                                                     |
grep '■'                                                            |
awk '{print $1,$2}'                                                  |
depthsort.sh -p7,27                                                  |
tee "$outfile"
